# Numbers
myint = 7
print(myint)

myfloat = 7.0
print(myfloat)

# Strings
mystring = 'hello'
print(mystring)
mystring = "hello"
print(mystring)

one = 1
two = 2
three = one + two
print(three)

hello = "hello"
world = "world"
helloworld = hello + " " + world
print(helloworld)

a, b = 3, 4
print(a, b)

